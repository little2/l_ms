function changeZoneByName(fieldname,objname) 
{ 
	
	obj=document.getElementById(objname);
	
	temindex = new Array(25);
	temval = new Array(25);
	var count=0; 
	
	c_temindex = new Array(25);
	c_temval = new Array(25);
	var c_count=0; 
	
	o_temindex = new Array(25);
	o_temval = new Array(25);
	var o_count=0;
	
	field_obj=document.getElementById(fieldname);

	province=document.getElementById(fieldname+"_province_code");
	city=document.getElementById(fieldname+"_city_code");
	office=document.getElementById(fieldname+"_office_code");
	
	
	if(obj)
	{
		if(obj.id==fieldname+"_province_code")
		{
			office.selectedIndex=0;
			city.selectedIndex=0;			
		}
		else if(obj.id==fieldname+"_city_code")
		{
			office.selectedIndex=0;
		}		

		var p_id=province.value.substring(0,2);
		var c_id=city.value.substring(2,4);
		var o_id=office.value.substring(4,6);
		field_obj.value=p_id+c_id+o_id;	
	}
	

	
	if((!field_obj) || (field_obj.value.length!=6))
	{
		field_obj.value='710000';	
	}		

	
	var s_p_id=field_obj.value.substring(0,2);
	var s_c_id=field_obj.value.substring(2,4);
	var s_o_id=field_obj.value.substring(4,6);			
		


	for(var key in CityCode)
	{
		if(key.substring(2,6)=="0000" )
		{	
			temindex[count]=key;	
			temval[count]=CityCode[key];
			count++;
		}
				
		if(key.substring(0,2)==s_p_id)
		{	
			if((key.substring(4,6)=="00" && key.substring(2,4)!="00") || key.substring(2,6)=="0000")
			{
				c_temindex[c_count]=key;	
				c_temval[c_count]=CityCode[key];
				c_count++;
			}
		}			

	
		
		if(key.substring(0,4)==(s_p_id+s_c_id))     
		{	
			o_temindex[o_count]=key;
			o_temval[o_count]=CityCode[key];
			o_count++;
		}	
		
		
		
	}
	
	province.length = count;
	city.length = c_count;
	office.length = o_count;
	
	for(i=0;i<count;i++)
	{
		province.options[i].text = temval[i];
		province.options[i].value = temindex[i];	
		

		if(province.options[i].value.substring(0,2)==s_p_id)
	    {
	    	province.selectedIndex=i;
	    }
	}
	
	for(j=0;j<c_count;j++)
	{
		city.options[j].text = c_temval[j];
		city.options[j].value = c_temindex[j];	
		
		if(city.options[j].value.substring(2,4)==s_c_id)
	    {
	    	city.selectedIndex=j;
	    }
	}	
	
	



		for(z=0;z<o_count;z++)
		{
			office.options[z].text = o_temval[z];
			office.options[z].value = o_temindex[z];	
			if(office.options[z].value.substring(4,6)==s_o_id)
		    {
		    	office.selectedIndex=z;
		    }			
		}	
		
	
	
}
